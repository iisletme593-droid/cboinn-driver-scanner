// çeviriyi (yoksa İngilizce, o da yoksa Türkçe kaynağı) döndürür. Dil değişince
// useLang() ile abone bileşenler yeniden render edilir.
import { useSyncExternalStore } from 'react';

export type Lang = 'tr' | 'en' | 'de' | 'ru' | 'ar';

/** Seçilebilir diller — her biri kendi dilindeki adıyla (endonim). */
export const LANGS: { code: Lang; label: string }[] = [
  { code: 'tr', label: 'Türkçe' },
  { code: 'en', label: 'English' },
  { code: 'de', label: 'Deutsch' },
  { code: 'ru', label: 'Русский' },
  { code: 'ar', label: 'العربية' },
];
const LANG_CODES: string[] = LANGS.map((l) => l.code);

/** Sağdan-sola yazılan diller (yalnızca Arapça). */
export function isRtl(l: Lang): boolean {
  return l === 'ar';
}

let current: Lang = 'tr';
const subs = new Set<() => void>();

export function setLang(l: Lang): void {
  const next: Lang = LANG_CODES.includes(l) ? l : 'tr';
  if (next === current) return;
  current = next;
  subs.forEach((fn) => fn());
}
export function getLang(): Lang {
  return current;
}
function subscribe(fn: () => void): () => void {
  subs.add(fn);
  return () => {
    subs.delete(fn);
  };
}
export function useLang(): Lang {
  return useSyncExternalStore(subscribe, getLang, getLang);
}

